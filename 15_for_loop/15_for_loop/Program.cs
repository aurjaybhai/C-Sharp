// For loop ==> repeats some code a finite amount of times

for (int i = 0; i < 20; i += 2)
{
    Console.WriteLine($"Number: {i}");
}

for (int i = 0 ; i < 20; i++)
{
    Console.WriteLine($"Number: {i}");
}

for (int i = 10 ; i > 0; i--)
{
    Console.WriteLine($"{i}");
}

Console.WriteLine("Happy New Year");

